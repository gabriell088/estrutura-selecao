/*
Como você pode usar uma declaração if para verificar se uma 
variável é do tipo string em JavaScript? 
*/


// Defina sua variável
var minhaVariavel = "Olá, mundo!";

// Verifique se a variável é do tipo string
if (typeof minhaVariavel === "string") {
    console.log("A variável é do tipo string.");
} else {
    console.log("A variável não é do tipo string.");
}
