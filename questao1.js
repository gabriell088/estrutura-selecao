/*
Como você pode usar uma declaração if para verificar se uma 
variável é maior que 10 em JavaScript? 
*/

// Defina sua variável
var numero = 15;

// Verifique se a variável é maior que 10
if (numero > 10) {
    console.log("A variável é maior que 10.");
} else {
    console.log("A variável não é maior que 10.");
}
