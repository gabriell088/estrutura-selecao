/*
Como você pode usar uma declaração if, else if, else para verificar 
se um número é positivo, negativo ou zero em JavaScript?
*/

// Defina seu número
var numero = 10;

// Verifique se o número é positivo, negativo ou zero
if (numero > 0) {
    console.log("O número é positivo.");
} else if (numero < 0) {
    console.log("O número é negativo.");
} else {
    console.log("O número é zero.");
}
